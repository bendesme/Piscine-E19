char	*ft_strcat(char *dest, char *src)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (dest[b])
	{
		++b;
	}
	while (src[i])
	{
		dest[b + i] = src[i];
		i++;
	}
	dest[b + i] = '\0';
	return (dest);
}
