int	ft_str_is_printable(char *str)
{
	int	c;

	c = 0;
	if (str[c] == '\0')
		return (1);
	while (str[c] != '\0')
	{
		if (str[c] >= 0 && str[c] < 32)
			return (0);
		c++;
	}
	return (1);
}
