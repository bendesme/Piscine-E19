int	ft_atoi(char *str)
{
	int	r;
	int	s;
	int	c;

	c = 0;
	r = 0;
	s = 1;
	while (str[c] == ' ' || (str[c] > 8 && str[c] < 14))
		c++;
	while (str[c] == '-' || str[c] == '+')
	{
		if (str[c] == '-')
			s = s * -1;
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		r = r * 10 + (str[c] - 48);
		c++;
	}
	return (r * s);
}

